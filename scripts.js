// Video --------------------------------------------
window.addEventListener("keydown", event => {

  if (event.key == "v") {
  document.getElementById('videoDiv').style.border = "thick solid black";
  }
  });

  window.addEventListener("keyup", event => {
  if (event.key == "v") {
  document.getElementById('videoDiv').style.border = "";
  }
});

